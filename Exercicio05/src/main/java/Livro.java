public class Livro {
        private String autor;
        private String titulo;


        public String getAutor() {
            return autor;
        }
        public String getTitulo() {
            return titulo;
        }

    public void setAutor(String pAutor) {
        this.autor = pAutor;
    }

    public void setTitulo(String pTitulo) {
        this.titulo = pTitulo;
    }
}
